package StringManipulation;

public class ConcateString {
	
	public static void main (String[] args) {
		
		
		String s1= "my name is";  
		String s2= " mansi darji";  
	 
		// Concatenates s2 to the end of s1.
		
		System.out.println("Concatenated string  = " + 
                s1.concat(s2));
}

}
